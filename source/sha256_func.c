#include "../headers/ft_ssl.h"

uint32_t	r64(uint32_t x, uint32_t y)
{
	return (((x >> y) | (x << (32 - y))));
}

uint32_t	s0(uint32_t x)
{
	return (r64(x, 7) ^ r64(x, 18) ^ (x >> 3));
}

uint32_t	s1(uint32_t x)
{
	return (r64(x, 17) ^ r64(x, 19) ^ (x >> 10));
}
